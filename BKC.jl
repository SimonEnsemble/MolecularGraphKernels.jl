# ╔═╡ 7b525f7e-cc83-4225-a3da-97579bad6a5d
function mpg_adj_mat(mpg::ProductGraph{Modular})::Matrix{Int}
	A = zeros(Int, nv(mpg), nv(mpg))
	for edge in edges(mpg)
		p = get_prop(mpg, edge, :label)
		@inbounds A[src(edge), dst(edge)] = p == 0 ? -2 : p
	end
	return A + A'
end

