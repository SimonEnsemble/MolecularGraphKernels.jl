begin
	function bkc_csi(M::Matrix{Int}; λ::Function=_ -> 1)::Int
	    function bkc_core(C::Vector{Int}, P::U, D::U, S::U) where {U <: BitVector}
	        value += λ(C)
	        if !any(P) && !any(S)
	            return
	        else
	            N = falses(n)
	            P′ = falses(n)
	            D′ = falses(n)
	            S′ = falses(n)
	            for uᵢ in findall(P)
	                @inbounds P[uᵢ] = false
	                P′ .= P
	                D′ .= D
	                S′ .= S
	                N .= false
	                @inbounds for v in findall(M[:, uᵢ] .≠ 0)
	                    N[v] = true
	                end
	                for v in findall(D′)
	                    @inbounds if M[uᵢ, v] ≠ 0 && M[uᵢ, v] ≠ -2
	                        @inbounds if T[v]
	                            @inbounds S′[v] = true
	                        else
	                            @inbounds P′[v] = true
	                        end
	                        @inbounds D′[v] = false
	                    end
	                end
	                bkc_core(C ∪ [uᵢ], P′ .& N, D′ .& N, S′ .& N)
	                @inbounds S[uᵢ] = true
	            end
	        end
	    end
	
	    value = 0
	    n = size(M, 1)
	    T = falses(n)
	    P = falses(n)
	    D = falses(n)
	    S = falses(n)
	    for u in 1:n
	        P .= false
	        D .= false
	        S .= false
	        @inbounds for v in findall(M[:, u] .≠ 0)
	            @inbounds if M[u, v] ≠ -2
	                @inbounds if T[v]
	                    @inbounds S[v] = true
	                else
	                    @inbounds P[v] = true
	                end
	            else
	                @inbounds D[v] = true
	            end
	        end
	        bkc_core([u], P, D, S)
	        @inbounds T[u] = true
	    end
	    return value
	end
	
	bkc_csi(Gₚ::ProductGraph{<:Union{Modular, Weighted}}; kwargs...) = bkc_csi(mpg_adj_mat(Gₚ); kwargs...)
end

